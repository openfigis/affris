              <td height="367" valign="top"><p align="left"><strong>Aquaculture Feed  and Nutrient Resources Information System</strong></p>
                  <p align="left">Aquaculture Feed and Nutrient  Resources Information System is intended to provide information on aquaculture  feed and nutrient resources and guidelines to use the information on the  feeding strategy.</p>
                  <p align="left">Each national aquaculture sector  must be made aware of:</p>
                  <div align="left">
                    <ul type="disc">
                      <li>● National agricultural feed and fertilizer resources       available to them,</li>
                      <li>● When, how and who is currently using these resources,</li>
                      <li>● Composition and cost of these resources at source and       with transportation,</li>
                      <li>● The status of the existing animal feed manufacturing       industry and its regulations, and</li>
                      <li>● How they obtain and best use the available feed       resources (whether they be cow dung, poultry manure, rice bran or imported       fishmeal) within their own dietary feeding strategies.</li>
                    </ul>
                  </div>
                  <ul type="disc">
                    <div align="left">Such an approach is essential if  countries are to maximize their use of locally available feed and fertilizer  resources, and so reduce their dependence upon imported fertilizers and feed  ingredient sources and ready-made compound aquafeeds, and consequently reduce  nutrient input costs and base feeding policies and strategies upon the use of  sustainable feed ingredient sources and feed lines.
                    </div>
                  </ul>
                  <p align="left">The information system will contain the following:<br />
                    <br />
                  1. Aquaculture feed and fertilizer resources: source, location, availability (seasonal and regional), utilization (human use, domestic animal and aquatic species); </p>
                  <p align="left">2. Nutrient composition of feed ingredients including amino acid, fatty acid and minerals; and information on toxicity and ant-nutritional factors inhibiting their use; and</p>
                  <p align="left">3. Aquaculture species information including their nutrient requirements and husbandry practices</p>
                  <p align="left">&nbsp;</p>
