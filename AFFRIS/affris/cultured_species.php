								  DIAS is the FAO Fisheries collections and database on Introductions of aquatic Species. This database includes records of aquatic species introduced or transferred from one country to another. Information can be accessed through a simple word search or advanced search using country names or species-introduction components. <a href="http://www.fao.org/fishery/dias/en">http://www.fao.org/fishery/dias/en</a><br />
