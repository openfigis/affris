								  GLOBEFISH is the unit in the FAO Fisheries and aquaculture Department responsible for information on international fish trade. The core GLOBEFISH is the GLOBEFISH databank. GLOBEFISH produces a number of publications including fish price reports (European Fish Price Report), market studies (GLOBEFISH Research Programme) and trend analysis (GLOBEFISH Highlights). GLOBEFISH is jointly financed by FAO and GLOBEFISH Partners, GLOBEFISH is and integral part of the FISH INFOnetwork and performs a co-ordinating role in the Network activities. <a href="http://www.globefish.org/">http://www.globefish.org/</a><br />
								  <br />
								  FishBase is a relational database with information to cater to different professionals such as research scientists, fisheries managers, zoologists and many more.  FishBase on the web contains practically all fish species known to science. <a href="http://www.fishbase.org/">http://www.fishbase.org/</a><br />
