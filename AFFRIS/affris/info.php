								<p><span class="style95">1. General Aquacultre Information: </span></p>
								<p>FAO is the only intergovernmental organization formally mandated by its constitution to undertake the worldwide collection, compilation, analysis and diffusion of data and information in fisheries and aquaculture. The compilation of accurate, relevant and timely data in a standard form facilitates monitoring, comparisons and analyses of status and trends that are essential to underpin the responsible development of the world's fisheries and aquaculture sectors and the sustainable utilization of the resources. It requires intensive international collaboration and cooperation, an area in which FAO plays a central role. <a href="http://www.fao.org/fishery/en">http://www.fao.org/fishery/en<br />
								</a><br />
