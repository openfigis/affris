								  This conference will provide a global forum for national, regional and international colleagues and government representatives to appreciate the current state of aquaculture development, to discuss how technology and resources, science, governance, networking, knowledge, manpower and institutional strengthening can contribute to finding solutions to the challenges the sector faces and will face in the future. <a href="http://www.aqua-conference2010.org">http://www.aqua-conference2010.org</a><br />
								  <br />
								  TheFishSite.com is an online news and technical resource to the global aquaculture industry. With a global readership the web site has become a good provider of free technical information to a rapidly growing worldwide audience. <a href="http://www.thefishsite.com">http://www.thefishsite.com</a><br />
								  <br />
								  Eurofish Magazine has become one of the most Europe’s widely distributed fisheries magazine. Published in English six times a year, the magazine provides comprehensive coverage of the trade, processing, and aquaculture sectors. Among the issues addressed are globalisation, illegal fishing, the consequences of global warming, sustainability, and organic labelling. Each issue of the magazine also features news, country reports, in-depth market analysis, technology and equipment reviews, and species profiles. <a href="http://www.eurofishmagazine.com">http://www.eurofishmagazine.com</a><br />
								  <br />
								  Aquaculture magazine is an international journal devoted to research on the exploration, improvement and management of all cultured aquatic food resources, both floristic and faunistic, from freshwater, brackish and marine environments, related directly or indirectly to humanconsumption. <a href="http://www.elsevier.com/wps/find/journaldescription.authors/503302">http://www.elsevier.com/wps/find/journaldescription.authors/503302</a><br />
								  <br />
								  One fish is a fishery projects portal and participatory resource gateway for the fisheries and aquatic research and development sector. <a href="http://www.onefish.org">http://www.onefish.org</a><br />
								  <br />
								  The Aquaculture Network Information Center (AquaNIC) was conceived in 1994 by the former USDA-Extension Service as one of the nation’s first network information centers to serve as a gateway to the world’s electronic aquaculture resources and  aquaculture information. <a href="http://www.onefish.org">http://aquanic.org</a><br />
								  <br />
								  An agency within the U.S. Department of Commerce, the National Oceanic and Atmospheric Administration (NOAA) has a long and rich tradition in aquaculture, from its support for cutting-edge science and research to federal policymaking. The Aquaculture library provides information of interest to scientists and others working with marine aquaculture. <a href="http://aquaculture.noaa.gov">http://aquaculture.noaa.gov</a><br />
								  <br />
								  AquaSol. Inc. is an aquaculture consulting company providing expert opinions, technology transfer and comprehensive project management services to the aquaculture industry through their website. <a href="http://www.fishfarming.com">http://www.fishfarming.com</a><br />
								  <br />
								  The Fish-Farmer magazine website is an accurate guide to all aquaculture news on an international level. <br />
								  <a href="http://www.fishfarmer-magazine.com">http://www.fishfarmer-magazine.com</a><br />
								  <br />
								  Fish Farming International is a monthly newspaper for the international aquaculture industry. The paper is redesigned as of July 2009 and covers aquaculture production, species, innovation and much more. <a href="http://www.intrafish.no/login">http://www.intrafish.no/login</a><br />
