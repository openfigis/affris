                    <td width="549" valign="top"><div align="justify">Aquaculture Feed and Nutrient Resources Information System is intended to provide information on aquaculture feed and nutrient resources and guidelines to use the information on the feeding strategy. <br />
                      <p>Each national aquaculture sector  must be made aware of:</p>
                              <ul type="disc">
                                <li>National agricultural feed and fertilizer resources       available to them,</li>
                                <li>When, how and who is currently using these resources,</li>
                                <li>Composition and cost of these resources at source and       with transportation,</li>
                                <li>The status of the existing animal feed manufacturing       industry and its regulations, and</li>
                                <li>How they obtain and best use the available feed       resources (whether they be cow dung, poultry manure, rice bran or imported       fishmeal) within their own dietary feeding strategies.</li>
                              </ul>
                              <p>Such an approach is essential if  countries are to maximize their use of locally available feed and fertilizer  resources, and so reduce their dependence upon imported fertilizers and feed  ingredient sources and ready-made compound aquafeeds, and consequently reduce  nutrient input costs and base feeding policies and strategies upon the use of  sustainable feed ingredient sources and feed lines.<br />
                                <span class="style58">.</span></p>
