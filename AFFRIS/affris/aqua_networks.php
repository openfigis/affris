								<p><span class="style95">2. Aquaculture Regional Networks: </span></p>
								<p>Network of Aquaculture Centres in Asia-Pacific. NACA is dedicated to rural development and poverty alleviation through sustainable aquaculture and aquatic resource management.<a href="http://www.enaca.org"> http://www.enaca.org</a><br />
								  <br />
								  The proposal made by the Committee for Inland Fisheries and Aquaculture (CIFAA) ad hoc Working Group on Aquaculture for the establishment of the Aquaculture Network for Africa (ANAF), a NACA-Like network for Africa, has been endorsed by the CIFAA, during its 14th Session held Accra, Ghana from 22-24 November 2006. <a href="http://www.anafaquaculture.org">http://www.anafaquaculture.org</a><br />
								  <br />
								  NACEE Network is a voluntary association of Central and Eastern European aquaculture institutions, in which all members maintain their full independence. Activities are coordinated by the Research Institute for Fisheries, Aquaculture and Irrigation ( HAKI ) of Hungary. All member institutions assign a liaison person who is in regular contact with the coordinating institution HAKI.<br /> 
								  <a href="http://www.agrowebcee.net/nacee">http://www.agrowebcee.net/nacee</a><br />
								  <br />
								  The European Aquaculture Society was launched at the occasion of the Tenth European Symposium of Marine Biologists, which took place in Oostende, Belgium, in 1975. EAS has four principal objectives: to promote contacts between all involved or interested in marine and freshwater aquaculture; to facilitate the circulation of aquaculture related information; to promote the sponsorship of multi-disciplinary research concerning aquaculture; and to enhance cooperation among governmental, scientific and commercial organizations and individuals on all matters dealing with aquaculture.  <a href="http://www.easonline.org">http://www.easonline.org</a><br />
								  <br />
								  World Aquaculture Society (WAS), an international non-profit organization with over 3,000 members in about 100 countries. Founded in 1969, the primary focus of WAS is to strengthen and facilitate communication and information exchange on high priority topics and emerging issues within the diverse global aquaculture community. The WAS has international chapters in the Asian Pacific region, Latin American and Caribbean region, United States, Japan and Korea. There are also numerous other organizations affiliated with WAS from diverse countries and regions worldwide. <a href="https://www.was.org/">https://www.was.org/</a><br />
								  <br />
								  European Network for the dissemination of Aquaculture RTD information. <a href="http://www.aquaflow.org/">http://www.aquaflow.org/</a><br />
